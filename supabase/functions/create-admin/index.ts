// Setup type definitions for built-in Supabase Runtime APIs
import "jsr:@supabase/functions-js/edge-runtime.d.ts"

import { serve } from "https://deno.land/std@0.168.0/http/server.ts"
import { createClient } from 'https://esm.sh/@supabase/supabase-js@2.7.1'

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    const supabaseClient = createClient(
      Deno.env.get('SUPABASE_URL') ?? '',
      Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? ''
    )

    // Admin kullanıcısını oluştur
    const { data: authUser, error: signUpError } = await supabaseClient.auth.admin.createUser({
      email: 'turalxaricde@xaricdeyasam.com',
      password: 'resadxaricde',
      email_confirm: true
    })

    if (signUpError) {
      throw signUpError
    }

    // Kullanıcı profilini admin rolüyle oluştur
    const { error: profileError } = await supabaseClient
      .from('users')
      .insert([
        {
          id: authUser.user.id,
          email: 'turalxaricde@xaricdeyasam.com',
          name: 'Tural Admin',
          role: 'admin'
        }
      ])

    if (profileError) {
      throw profileError
    }

    return new Response(
      JSON.stringify({ message: 'Admin kullanıcısı başarıyla oluşturuldu' }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 200,
      },
    )
  } catch (error) {
    return new Response(
      JSON.stringify({ error: error.message }),
      {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        status: 400,
      },
    )
  }
})

